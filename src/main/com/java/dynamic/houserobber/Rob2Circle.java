/**
 * leet 198. 打家劫舍2，房屋排成一个圈
 */
public class Rob2Circle {
    public int rob(int[] nums) {
        int n = nums.length;
        /**
         * 围成一圈，第一和最后一家相连，相邻房屋不能抢
         * 则取 0 到 n-2 最优值 v1；1 到 n-1的最优值 v2；
         * res = Math.max(v1, v2);
         */
        return Math.max(rangeOfRob(nums, 0, n - 2 ), rangeOfRob(nums, 1, n -1));
    }

    public int rangeOfRob(int[] nums, int start, int end ) {
        int dp_i_2 = 0, dp_i_1 = 0, dp_i = 0;
        for (int i = end; i >= start; i--) {
            dp_i = Math.max(dp_i_1, nums[i] + dp_i_2);
            dp_i_2 = dp_i_1;
            dp_i_1 = dp_i;
        }
        return dp_i;
    }

}
